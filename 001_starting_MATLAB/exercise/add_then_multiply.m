function result = add_then_multiply(a, b, c)
% ADD_THEN_MULTIPLY add two numbers a and b, and multiply the sum by c.
%
%   result = add_then_multiply(a1, a2, m);
%   Input:
%       a, b : the numbers to be added
%       c    : the number to be multiplied
%   Output:
%       result : the answer after adding a and b and multiplying by c
%
    result = (a + b) * c;
end
